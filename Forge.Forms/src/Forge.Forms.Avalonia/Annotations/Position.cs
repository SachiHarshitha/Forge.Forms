namespace Forge.Forms.AvaloniaUI.Annotations;

/// <summary>
///     Specifies the position of an element in its row.
/// </summary>
public enum Position
{
    /// <summary>
    ///     The content is aligned from the right. Actions have right alignment by default.
    /// </summary>
    Right,

    /// <summary>
    ///     The content is aligned from the left.
    /// </summary>
    Left
}